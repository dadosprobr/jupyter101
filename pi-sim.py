from random import uniform
import sys

def pi(n):
    c = 0
    i = 0
    while i < n:
        if abs(complex(uniform(-1, 1), uniform(-1, 1))) <= 1:
            c += 1
        i += 1
    return c * 4.0 / n


for i in range(10):
    n = 10**i
    print(n, end=' ')
    sys.stdout.flush()
    print(pi(n))

